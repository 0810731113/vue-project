<template>
  <base-main
    :list="list"
    :pageIndex="pageIndex"
    :pageSize="pageSize"
    :recordCount="recordCount"
    type="billnote"
    :onChangeSize="onChangeSize"
    :onChangeCurrent="onChangeCurrent"
    :onClickDelete="onClickDelete"
  >
  </base-main>
</template>

<script>
import { mapActions, mapState } from 'vuex';
import { BaseMain } from '../components';
import { DEFAULT_PAGE } from 'constant/pagination';

export default {
  name: 'BillNote',
  components: {
    BaseMain
  },
  computed: mapState({
    list: state => state.businessSetting.billNote.list,
    recordCount: state => state.businessSetting.billNote.recordCount,
    pageIndex: state => state.businessSetting.billNote.pageIndex,
    pageSize: state => state.businessSetting.billNote.pageSize
  }),
  async created () {
    const { pageIndex, pageSize } = this;
    await this.fetchAllBillNote({ pageIndex, pageSize });
  },
  methods: {
    ...mapActions([
      'fetchAllBillNote',
      'deleteBillNote'
    ]),
    async onChangeSize (pageSize) {
      await this.fetchAllBillNote({ pageIndex: DEFAULT_PAGE, pageSize });
    },
    async onChangeCurrent (pageIndex) {
      await this.fetchAllBillNote({ pageIndex, pageSize: this.pageSize });
    },
    async onClickDelete (id) {
      const { pageIndex, pageSize } = this;
      await this.deleteBillNote(id);
      await this.fetchAllBillNote({ pageIndex, pageSize });
    }
  }
};
</script>

